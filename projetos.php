    <p> O projeto tem como objetivo controlar a iluminação em estufas utilizando um <br>
    sensor de iluminação LDR e LED. Quando a iluminação estiver abaixo de 400 lux, o <br>
    LED azul será aceso. Se a iluminação estiver acima de 400 lux, o LED vermelho será <br>
    aceso. Essa mudança de iluminação permite a transferência da voltagem do sensor para o<br>
    LED, promovendo seu acionamento. As cores dos LEDs foram escolhidas com base em pesquisas<br>
    científicas que comprovam que as cores vermelha e azul são fundamentais para o crescimento<br>
    das plantas. Além disso, é possível monitorar a luminosidade nas estufas de forma remota por<br> 
    meio do MQTT Explorer.</p>
